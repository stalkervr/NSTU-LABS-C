#include <stdio.h>
#include <string.h>

typedef struct
{
    char nm[60];
    unsigned char age;
    unsigned char course;
} student;

student addStudent(const char ch[], unsigned char age, unsigned char course);

int main()
{
    student st[20];

    st[0] = addStudent("Test Test Test", 2, 18);

    return 0;
}

student addStudent(char ch[], unsigned char age, unsigned char course)
{
    student res_st;
    strcpy(res_st.nm, ch);
    res_st.age = age;
    res_st.course = course;
    return res_st;
}

